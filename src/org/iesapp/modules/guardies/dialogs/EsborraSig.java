    private void checkSignatures()
    {
         if(!isListening) {
            return;
        }
        
        jTextArea1.append("Dia "+jDateChooser1.getDate()+"\n");
        //Comprova si el dia triat t√© o no signatures
        String SQL1 = "Select count(id) as total from sig_signatures where data='"+new DataCtrl(jDateChooser1.getDate()).getDataSQL()+"'";
        try {
            Statement st =coreCfg.getMysql().createStatement();
            ResultSet rs1 =coreCfg.getMysql().getResultSet(SQL1,st);
            if(rs1!=null && rs1.next())
            {
                jTextArea1.append("> N√∫m. signatures mat√≠="+rs1.getInt("total")+"\n");
            }
            if(rs1!=null) {
                rs1.close();
                st.close();
            }
        } catch (SQLException ex) {
            Logger.getLogger(EsborraSig.class.getName()).log(Level.SEVERE, null, ex);
        }
            //Comprova si el dia triat t√© o no signatures
        SQL1 = "Select count(id) as total from sig_signatures_tarda where data='"+new DataCtrl(jDateChooser1.getDate()).getDataSQL()+"'";
        try {
            Statement st =coreCfg.getMysql().createStatement();
            ResultSet rs1 =coreCfg.getMysql().getResultSet(SQL1,st);
            if(rs1!=null && rs1.next())
            {
                jTextArea1.append("> N√∫m. signatures tarda="+rs1.getInt("total")+"\n");
            }
            if(rs1!=null) {
                rs1.close();
                st.close();
            }
        } catch (SQLException ex) {
            Logger.getLogger(EsborraSig.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTextArea1.append("\n");
    }
    
    
